import argparse
import base64
import logging
import os
import re
from pathlib import Path
from datetime import datetime
from dotenv import load_dotenv
import litellm
import sys
from rich.logging import RichHandler

# Ensure the parent directory is in the path to allow absolute imports
sys.path.insert(0, str(Path(__file__).parent.parent))

# Load API keys from .env if present
load_dotenv()

from research_harness.llm_utils import get_llm_kwargs

# Configure logging to write to STDOUT so the orchestrator can capture it
logging.basicConfig(
    level=logging.INFO,
    format="%(message)s",
    datefmt="[%X]",
    handlers=[RichHandler(console=None, show_time=False, show_path=False, markup=True)]
)
logger = logging.getLogger("VisionInterpreter")

def encode_image(image_path: Path) -> str:
    """Encodes an image to base64."""
    with open(image_path, "rb") as image_file:
        return base64.b64encode(image_file.read()).decode('utf-8')

def main():
    parser = argparse.ArgumentParser(description="Vision LLM Interpreter for Visualizations (AP-RH6).")
    parser.add_argument("--figures-dir", default="research_insights/figures", help="Directory containing PNG figures to analyze.")
    parser.add_argument("--provider", default="anthropic", help="The LiteLLM provider (e.g., anthropic, openai, gemini).")
    parser.add_argument("--model", default="claude-3-5-sonnet-20241022", help="The vision-capable model string.")
    parser.add_argument("--out-dir", default="research_insights", help="Directory for output.")
    args = parser.parse_args()
    
    figures_dir = Path(args.figures_dir)
    out_dir = Path(args.out_dir)
    log_dir = out_dir / "logs"
    
    if not figures_dir.exists():
        logger.error(f"Figures directory not found: {figures_dir}")
        return
        
    png_files = list(figures_dir.glob("*.png"))
    if not png_files:
        logger.warning(f"No PNG files found in {figures_dir}")
        return
        
    log_dir.mkdir(parents=True, exist_ok=True)
    
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    log_file = log_dir / f"vision_thought_{timestamp}.txt"
    out_file = out_dir / f"visualization_interpretations_{timestamp}.md"
    
    model_uri = f"{args.provider}/{args.model}"
    logger.info(f"Invoking Vision Model {model_uri} via LiteLLM to analyze {len(png_files)} images...")
    
    # Initialize the output markdown file
    with open(out_file, "w") as f:
        f.write(f"# VISUALIZATION INTERPRETATIONS (Generated by {model_uri})\n\n")
        
    prompt_file = Path(__file__).parent / "prompts" / "vision_prompt.txt"
    with open(prompt_file, "r") as pf:
        vision_prompt = pf.read()
        
    for img_path in sorted(png_files):
        logger.info(f"Analyzing {img_path.name}...")
        
        try:
            base64_image = encode_image(img_path)
            
            messages = [
                {
                    "role": "user",
                    "content": [
                        {"type": "text", "text": vision_prompt},
                        {
                            "type": "image_url",
                            "image_url": {
                                "url": f"data:image/png;base64,{base64_image}"
                            }
                        }
                    ]
                }
            ]
            
            base_kwargs = {
                "messages": messages,
                "temperature": 0.2
            }
            
            kwargs = get_llm_kwargs(args.provider, args.model, base_kwargs)
            response = litellm.completion(**kwargs)
            
            insight_text = response.choices[0].message.content
            
            # Parse out the <thought> block
            thought_match = re.search(r'<thought>(.*?)</thought>', insight_text, re.DOTALL)
            if thought_match:
                thought_content = thought_match.group(1).strip()
                insight_text = re.sub(r'<thought>.*?</thought>\s*', '', insight_text, flags=re.DOTALL).strip()
                
                with open(log_file, "a") as f:
                    f.write(f"\n\n=== THOUGHT PROCESS FOR {img_path.name} ===\n")
                    f.write(thought_content)
            else:
                logger.warning(f"No <thought> block found for {img_path.name}")
                
            with open(out_file, "a") as f:
                f.write(f"## Analysis of `{img_path.name}`\n\n")
                f.write(f"![{img_path.name}](figures/{img_path.name})\n\n")
                f.write(insight_text)
                f.write("\n\n---\n\n")
                
            logger.info(f"Successfully appended interpretation for {img_path.name}")
            
        except Exception as e:
            logger.error(f"Failed to analyze {img_path.name}: {e}")
            
    logger.info(f"All interpretations completed and saved to {out_file}")

if __name__ == "__main__":
    main()
